package JavaPackage;

public class LoopsinJava {
    public static void main(String[] args){
//While loop
       int i=1;
       while(i<=10)
       {
           System.out.println(i);
           i++;
       }

       //for Loop
        for (int k=10; k>=-10; k--) {
            System.out.println("value of K " + k);
        }
    }
}
